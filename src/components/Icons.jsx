// Hämta ikon baserat på kategori
export function getCategoryIcon(iconName, size = 20) {
  const icons = {
    music: MusicIcon,
    sport: SportIcon,
    mic: MicIcon,
    calendar: CalendarIcon,
    hockey: HockeyIcon,
    football: FootballIcon,
    gaming: GamingIcon,
    theater: TheaterIcon,
    ticket: TicketIcon
  };
  const Icon = icons[iconName] || CalendarIcon;
  return <Icon size={size} />;
}

